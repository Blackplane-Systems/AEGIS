function numberValue(value: unknown): number | undefined {
  return typeof value === 'number' && Number.isFinite(value) ? value : undefined;
}

function stringValue(value: unknown): string {
  return typeof value === 'string' ? value : '';
}

function truthy(value: unknown): boolean {
  return value === true || value === 'true' || value === 1;
}

function stringArray(value: unknown): readonly string[] {
  if (!Array.isArray(value)) {
    return [];
  }
  return value.filter((item): item is string => typeof item === 'string' && item.length > 0);
}

function routingProtocolValue(value: unknown): RoutingProtocol | undefined {
  if (typeof value !== 'string') {
    return undefined;
  }
  const upper = value.toUpperCase();
  if (
    upper === 'STATIC' ||
    upper === 'BGP' ||
    upper === 'OSPF' ||
    upper === 'RIP' ||
    upper === 'MESH' ||
    upper === 'MQTT_BROKER' ||
    upper === 'LORA_GATEWAY' ||
    upper === 'ESP_NOW_PEER' ||
    upper === 'SERIAL_MULTIPLEX' ||
    upper === 'UNKNOWN'
  ) {
    return upper;
  }
  return undefined;
}

function addressFamily(value: unknown): NetworkIntelligenceObservation['addressFamily'] {
  if (value === 'IPv4' || value === 'IPv6' || value === 'DUAL_STACK' || value === 'UNKNOWN') {
    return value;
  }
  return undefined;
}
